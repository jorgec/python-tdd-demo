class Room:
    ################################################################################
    # Protected Fields
    ################################################################################
    __name: str

    ################################################################################
    # Public Fields
    ################################################################################

    ################################################################################
    # Getters and Setters
    ################################################################################

    @property
    def name(self) -> str:
        return self.__name

    @name.setter
    def name(self, s: str) -> None:
        if not s:
            raise ValueError("Name can't be empty!")
        self.__name = s

    ################################################################################
    # Dunder Methods
    ################################################################################
    def __init__(self, name: str):
        self.name = name

    ################################################################################
    # Class Methods
    ################################################################################
